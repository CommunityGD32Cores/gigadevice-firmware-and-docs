/*!
    \brief      this function handles LVD exception
    \param[in]  none
    \param[out] none
    \retval     none
*/
void LVD_IRQHandler(void)
{
    if(RESET != exti_interrupt_flag_get(EXTI_16)){
        gd_eval_led_toggle(LED1);
        exti_interrupt_flag_clear(EXTI_16);
    }
}
