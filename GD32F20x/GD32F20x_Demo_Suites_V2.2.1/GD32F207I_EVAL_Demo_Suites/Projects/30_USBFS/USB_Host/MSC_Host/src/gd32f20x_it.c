/*!
    \brief      this function handles external line 1 interrupt exception
    \param[in]  none
    \param[out] none
    \retval     none
*/
void EXTI1_IRQHandler(void)
{
    if (RESET != exti_interrupt_flag_get(EXTI_1)) {
        usb_host.usr_cb->dev_over_currented();

        exti_interrupt_flag_clear (EXTI_1);
    }
}

